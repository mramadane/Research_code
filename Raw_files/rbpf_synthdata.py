# ---------------- CONTINUOUS-TIME MATRICES FROM θ (5-state) ---------------
def continuous_from_theta(theta_arr):
    """
    Build continuous-time A_c and input vectors B_u, B_To, B_Us
    for the 5-state RC model from per-particle θ.
    theta_arr shape: (Np, 10) with
      [Ci, Cm, Ce, Ch, Cs, g_im, g_ie, g_ih, g_is, g_ea]
    Returns:
      A_c   : (Np, 5, 5)
      B_u   : (Np, 5)  (heater power)
      B_To  : (Np, 5)  (outdoor temp)
      B_Us  : (Np, 5)  (solar)
    """
    Np_loc = theta_arr.shape[0]

    Ci  = theta_arr[:, 0]
    Cm  = theta_arr[:, 1]
    Ce  = theta_arr[:, 2]
    Ch  = theta_arr[:, 3]
    Cs  = theta_arr[:, 4]
    gim = theta_arr[:, 5]
    gie = theta_arr[:, 6]
    gih = theta_arr[:, 7]
    gis = theta_arr[:, 8]
    gea = theta_arr[:, 9]
    gia = np.zeros_like(gea)

    invC = np.stack([1.0/Ci, 1.0/Cm, 1.0/Ce, 1.0/Ch, 1.0/Cs], axis=1)

    # Conductance G entries
    G00 = -(gim + gie + gih + gis + gia)
    G01 = gim; G02 = gie; G03 = gih; G04 = gis
    G10 = gim; G11 = -gim
    G20 = gie; G22 = -(gie + gea)
    G30 = gih; G33 = -gih
    G40 = gis; G44 = -gis
    Z0  = np.zeros_like(G00)

    # A_c rows (then scaled by 1/C per row)
    A0 = np.stack([G00, G01, G02, G03, G04], axis=1) * invC[:, [0]]
    A1 = np.stack([G10, G11, Z0,  Z0,  Z0 ], axis=1) * invC[:, [1]]
    A2 = np.stack([G20, Z0,  G22, Z0,  Z0 ], axis=1) * invC[:, [2]]
    A3 = np.stack([G30, Z0,  Z0,  G33, Z0 ], axis=1) * invC[:, [3]]
    A4 = np.stack([G40, Z0,  Z0,  Z0,  G44], axis=1) * invC[:, [4]]
    A_c = np.stack([A0, A1, A2, A3, A4], axis=1)   # (Np, 5, 5)

    # Inputs (continuous-time):
    # B_u: heater input (u -> Th)
    B_u  = np.zeros((Np_loc, 5), float)
    B_u[:, 3] = eta_h / Ch

    # B_To: outdoor temperature
    B_To = np.zeros((Np_loc, 5), float)
    B_To[:, 0] = 0.0 * (1.0 / Ci)          # g_ia = 0
    B_To[:, 2] = gea * (1.0 / Ce)

    # B_Us: solar
    B_Us = np.zeros((Np_loc, 5), float)
    B_Us[:, 0] = Aw * (1.0 / Ci)
    B_Us[:, 2] = Ae * (1.0 / Ce)

    return A_c, B_u, B_To, B_Us

# ---------------- RBPF loop (Euler sub-stepping: 60s) ----------------

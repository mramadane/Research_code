def arrays_size_mb(arr_list, dedupe_by_data_ptr=True):
    """
    Sum payload sizes (nbytes) of NumPy arrays in arr_list.
    Returns total size in MB.
    """
    total = 0
    seen = set()

    for a in arr_list:
        if a is None:
            continue
        if not isinstance(a, np.ndarray):
            continue

        if dedupe_by_data_ptr:
            # pointer to underlying data buffer
            ptr = int(a.__array_interface__["data"][0])
            if ptr in seen:
                continue
            seen.add(ptr)

        total += a.nbytes

    return total / (1024**2)

def generate_square_heating(N,
                            dt_seconds,
                            power=6000.0,
                            p_start=0.002,                # per-STEP start prob when OFF
                            min_duration_s=30*60,         # 30 minutes
                            max_duration_s=4*60*60,       # 4 hours
                            seed=None):
    """
    Generate a square heating signal with random ON episodes.
    - When OFF, each step has probability p_start to begin an ON episode.
    - Each ON episode has a constant 'power' and a duration uniform in [min_duration_s, max_duration_s].
    - Episodes do not overlap; new ones can start only when the previous one ends.

    Returns
    -------
    F_true : (N,) float array
        Heating power in Watts at each step.
    event_log : list of dict
        Each dict has: {'start_idx', 'end_idx', 'duration_s'} (end_idx inclusive).
    """
    rng = np.random.default_rng(seed)

    # durations in steps
    min_steps = int(np.ceil(min_duration_s / dt_seconds))
    max_steps = int(np.ceil(max_duration_s / dt_seconds))
    print("MAX steps",max_steps)
    print("MIN steps",min_steps)
    if min_steps < 1:
      min_steps = 1
    if max_steps < min_steps:
      max_steps = min_steps

    F_true = np.zeros(N, dtype=float)
    event_log = []

    on_remaining = 0
    start_idx = None

    for k in range(N):
        if on_remaining > 0:
            F_true[k] = power
            on_remaining -= 1
            if on_remaining == 0:
                # episode ends at k
                event_log.append({
                    "start_idx": start_idx,
                    "end_idx": k,
                    "duration_s": (k - start_idx + 1) * dt_seconds
                })
                start_idx = None
        else:
            # currently OFF; possibly start a new episode
            if rng.random() < p_start:
                dur_steps = rng.integers(min_steps, max_steps + 1)
                on_remaining = dur_steps
                start_idx = k
                F_true[k] = power
                on_remaining -= 1  # consumed this step
            # else remains 0 (OFF)
    # If an ON episode runs past the last index, we close it at N-1
    if start_idx is not None and (len(event_log) == 0 or event_log[-1]["end_idx"] != N-1):
        event_log.append({
            "start_idx": start_idx,
            "end_idx": N-1,
            "duration_s": (N - start_idx) * dt_seconds
        })

    return F_true, event_log

def generate_pulse_heating(N,
                           dt_seconds,
                           mode="alpha",                  # "alpha" (rise/decay) or "gauss"
                           p_start=0.0015,                # per-step birth probability
                           amp_range=(1500.0, 8000.0),    # Watts (uniform)
                           tau_r_range_s=(5*60, 40*60),   # alpha: rise time (seconds)
                           tau_d_range_s=(45*60, 4*60*60),# alpha: decay time (seconds)
                           sigma_range_s=(15*60, 90*60),  # gauss: std (seconds)
                           kill_rel=1e-3,                 # drop pulse when value < kill_rel * A
                           max_active=6,                  # cap concurrent pulses
                           seed=None):
    """
    Generate a bursty positive latent heat signal as a sum of smooth pulses.

    Pulses arrive via a Bernoulli hazard p_start per step. Each new pulse samples its
    parameters from simple ranges, remains active, and contributes a smooth, positive shape:

      - mode="alpha":  f(t) = A * max( exp(-age/tau_d) - exp(-age/tau_r), 0.0 )
      - mode="gauss":  f(t) = A * exp( -0.5 * (age/sigma)**2 )

    A pulse is dropped when f(t) < kill_rel * A.

    Returns
    -------
    F_true : (N,) float
        Total latent heat per step (W).
    event_log : list of dict
        For each pulse:
          {
            'mode', 'start_idx', 'end_idx',
            'A', 'tau_r_s', 'tau_d_s', 'sigma_s',
            'peak_W', 'peak_idx'
          }
        Unused keys are None for the chosen mode.
    """
    import numpy as np

    rng = np.random.default_rng(seed)
    F_true = np.zeros(N, dtype=float)
    event_log = []

    active = []  # list of dicts; each has fixed params and evolving 'age_s'

    def _sample_time_s(lo, hi):
        # sample in log-space for robustness if ranges are wide
        lo = max(lo, 1.0)
        hi = max(hi, lo + 1.0)
        return float(10 ** rng.uniform(np.log10(lo), np.log10(hi)))

    for k in range(N):
        # births
        if len(active) < max_active and rng.random() < p_start:
            A = rng.uniform(*amp_range)
            if mode == "alpha":
                tau_r_s = _sample_time_s(*tau_r_range_s)
                tau_d_s = _sample_time_s(*tau_d_range_s)
                if tau_d_s <= tau_r_s:
                    tau_d_s = tau_r_s * rng.uniform(1.5, 3.0)  # enforce decay > rise
                pulse = {
                    "mode": "alpha",
                    "A": A,
                    "tau_r_s": tau_r_s,
                    "tau_d_s": tau_d_s,
                    "sigma_s": None,
                    "age_s": 0.0,
                    "start_idx": k,
                    "peak_W": 0.0,
                    "peak_idx": k
                }
            elif mode == "gauss":
                sigma_s = _sample_time_s(*sigma_range_s)
                pulse = {
                    "mode": "gauss",
                    "A": A,
                    "tau_r_s": None,
                    "tau_d_s": None,
                    "sigma_s": sigma_s,
                    "age_s": 0.0,
                    "start_idx": k,
                    "peak_W": 0.0,
                    "peak_idx": k
                }
            else:
                raise ValueError("mode must be 'alpha' or 'gauss'")
            active.append(pulse)

        # accumulate contributions from active pulses at time k
        val_k = 0.0
        keep = []
        for p in active:
            age = p["age_s"]
            A = p["A"]
            if p["mode"] == "alpha":
                tr = p["tau_r_s"]
                td = p["tau_d_s"]
                f = A * max(np.exp(-age/td) - np.exp(-age/tr), 0.0)
            else:  # "gauss"
                sg = p["sigma_s"]
                f = A * np.exp(-0.5 * (age/sg)**2)

            val_k += f

            # track peak and decide if to keep
            if f > p["peak_W"]:
                p["peak_W"] = f
                p["peak_idx"] = k

            if (age == 0.0) or (f >= kill_rel * A):
                p["age_s"] = age + dt_seconds
                keep.append(p)
            else:
                p["end_idx"] = k
                event_log.append(p)
        active = keep
        F_true[k] = val_k

    # close any remaining pulses at the end
    for p in active:
        p["end_idx"] = N - 1
        event_log.append(p)

    # normalize event_log fields
    for p in event_log:
        p.setdefault("end_idx", N - 1)
        p.setdefault("peak_W", 0.0)
        p.setdefault("peak_idx", p["start_idx"])

    return F_true, event_log


    for k in range(N_total):
        if quiet_mask_arr[k]:
            if not in_seg:
                in_seg = True
                start_idx = k
        else:
            if in_seg:
                end_idx = k - 1
                seg_len = end_idx - start_idx + 1
                if seg_len >= min_quiet_steps:
                    quiet_segments.append((start_idx, end_idx))
                in_seg = False

    # Handle case where we end in a quiet segment
    if in_seg:
        end_idx = N_total - 1
        seg_len = end_idx - start_idx + 1
        if seg_len >= min_quiet_steps:
            quiet_segments.append((start_idx, end_idx))

    print("\n=========== Open-loop forecast errors on quiet intervals (F_true ≈ 0) ===========")
    print(f"dt = {dt_seconds} s, min_quiet_steps = {min_quiet_steps} (~{min_quiet_hours:.1f} h)")


def _continuous_from_theta_single(theta):
    """
    Continuous-time A_c, B_c for a *single* θ vector:
      θ = [Ci, Cm, Ce, Ch, Cs, g_im, g_ie, g_ih, g_is, g_ea]
    Inputs (columns of B_c): [u (heater), T_out, U_s]
    Returns:
      A_c: (5,5)
      B_c: (5,3)
    """
    Ci, Cm, Ce, Ch, Cs = theta[0:5]
    g_im, g_ie, g_ih, g_is, g_ea = theta[5:10]
    g_ia = 0.0

    invC = np.array([1.0/Ci, 1.0/Cm, 1.0/Ce, 1.0/Ch, 1.0/Cs])

    # --- G matrix entries (symmetric conductances) ---
    G00 = -(g_im + g_ie + g_ih + g_is + g_ia)
    G01 = g_im
    G02 = g_ie
    G03 = g_ih
    G04 = g_is

    G10 = g_im
    G11 = -g_im

    G20 = g_ie
    G22 = -(g_ie + g_ea)

    G30 = g_ih
    G33 = -g_ih

    G40 = g_is
    G44 = -g_is

    # Assemble G as 5x5
    A_c = np.zeros((5, 5), float)
    A_c[0, :] = [G00, G01, G02, G03, G04]

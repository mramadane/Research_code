def compute_input_psd_on_grid(u, w_grid, dt):
    """
    Compute an (unnormalized) one-sided PSD of the input sequence u,
    and interpolate it onto the angular frequency grid w_grid [rad/s].

    Any overall scaling cancels in the relative band selection, so we
    do not worry about exact normalization constants here.
    """
    u = np.asarray(u, float).ravel()
    u = u - np.mean(u)

    N = len(u)
    U = np.fft.rfft(u)
    Su_fft = np.abs(U)**2           # proportional to PSD
    f_fft = np.fft.rfftfreq(N, d=dt)

    # Map w_grid [rad/s] to Hz and interpolate PSD
    f_grid = w_grid / (2.0 * np.pi)
    Su_grid = np.interp(f_grid, f_fft, Su_fft, left=0.0, right=0.0)

    return Su_grid


def prepare_tf_band_info(theta_true, channel, u_input, w_grid,
                         energy_frac=0.99, use_full_band=False):
    """
    For a given channel, build the true TF, compute the input PSD on the
    same frequency grid, and determine the band over which TF errors
    will be evaluated.

    If use_full_band is False:
        - Determine the smallest band [0, w_cut] that contains
          `energy_frac` of the true output energy:
              E_true(ω) = |G_true(e^{jω})|^2 * S_u(ω)

    If use_full_band is True:
        - Use the full band [0, Nyquist] (i.e. all w_grid points).

    Returns a dict with:
        Su_grid   : PSD on w_grid
        band_mask : boolean mask selecting the band
        true_norm : RMS magnitude of G_true over that band
        f_cut     : cutoff frequency in Hz (w_cut / 2π)
        f_nyq     : Nyquist frequency in Hz (1 / (2*dt))
    """
    sys_true = build_siso_ss_from_theta(theta_true, channel)
    mag_true, phase_true, _ = control.freqresp(sys_true, w_grid)
    G_true = mag_true.squeeze() * np.exp(1j * phase_true.squeeze())

    # Input PSD on grid
    Su_grid = compute_input_psd_on_grid(u_input, w_grid, dt_seconds)

    if use_full_band:
        band_mask = np.ones_like(w_grid, dtype=bool)
    else:
        # True output energy density = |G_true|^2 * S_u
        E_true = (np.abs(G_true)**2) * Su_grid
        E_tot = np.sum(E_true)

        if not np.isfinite(E_tot) or E_tot <= 0.0:
            # Fallback: no meaningful energy, keep full band
            band_mask = np.ones_like(w_grid, dtype=bool)
        else:
            cum = np.cumsum(E_true)
            ratio = cum / E_tot
            k_cut = int(np.searchsorted(ratio, energy_frac))
            k_cut = min(max(k_cut, 0), len(w_grid) - 1)
            band_mask = np.zeros_like(w_grid, dtype=bool)
            band_mask[:k_cut + 1] = True

    G_true_band = G_true[band_mask]
    true_norm = float(np.sqrt(np.mean(np.abs(G_true_band)**2))) if G_true_band.size > 0 else 0.0

    w_cut = w_grid[band_mask].max()
    f_cut = float(w_cut / (2.0 * np.pi))
    f_nyq = float(1.0 / (2.0 * dt_seconds))

    return {
        "Su_grid": Su_grid,
        "band_mask": band_mask,
        "true_norm": true_norm,
        "f_cut": f_cut,
        "f_nyq": f_nyq,
    }


def tf_naive_errors(theta_est, theta_true, channel, w_grid, band_info, eps=1e-8):
    """
    Naive band-limited TF errors for one channel, on the chosen band
    (either the ENERGY_FRAC-band of true output energy, or the full band).

    We compute:
      - abs_err   : RMS of |G_est - G_true| over the band
      - rel_err   : RMS of |(G_est - G_true) / G_true| over the band
      - hinf_abs  : max_ω |G_est - G_true| over the band (H_infinity-like)
      - hinf_rel  : hinf_abs / max_ω |G_true| over the band
    """
    sys_true = build_siso_ss_from_theta(theta_true, channel)
    sys_est  = build_siso_ss_from_theta(theta_est,  channel)

    mag_true, phase_true, _ = control.freqresp(sys_true, w_grid)
    mag_est,  phase_est,  _ = control.freqresp(sys_est,  w_grid)

    G_true = mag_true.squeeze() * np.exp(1j * phase_true.squeeze())
    G_est  = mag_est.squeeze()  * np.exp(1j * phase_est.squeeze())

    mask = band_info["band_mask"]
    G_true_band = G_true[mask]
    G_est_band  = G_est[mask]

    if G_true_band.size == 0:
        return np.nan, np.nan, np.nan, np.nan

    diff = G_est_band - G_true_band

    # Absolute RMS error: RMS of |G_est - G_true|
    abs_err = float(np.sqrt(np.mean(np.abs(diff)**2)))

    # Pointwise relative error; guard against |G_true| ~ 0
    denom = np.where(np.abs(G_true_band) < eps, np.nan, G_true_band)
    rel_pt = diff / denom
    rel_pt = rel_pt[~np.isnan(rel_pt)]

    if rel_pt.size == 0:
        rel_err = np.nan
    else:
        rel_err = float(np.sqrt(np.mean(np.abs(rel_pt)**2)))

    # H_infinity-like errors over the same band
    hinf_abs = float(np.max(np.abs(diff)))
    max_true = float(np.max(np.abs(G_true_band))) if G_true_band.size > 0 else 0.0
    hinf_rel = float(hinf_abs / max_true) if max_true > eps else np.nan

    return abs_err, rel_err, hinf_abs, hinf_rel



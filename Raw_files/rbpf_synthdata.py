for k in range(1, N):
    uk, Tok, Usk = u_arr[k-1], T_out_K[k-1], U_s_arr[k-1]
    yk = float(Y_meas[k])

    # Prior weights
    w_lin_prior = np.exp(logw - logsumexp(logw))

    # (A) Global SV: random walk on log_alpha (always active)
    sigma_eta_eff = sigma_eta_post
    log_alpha = log_alpha + rng.normal(0.0, sigma_eta_eff, size=Np)
    log_alpha = np.clip(log_alpha, np.log(alpha_min), np.log(alpha_max))
    alpha     = np.exp(log_alpha)

    # Effective α for gating: weighted median under PRIOR weights
    alpha_med_k = wquantile(alpha, w_lin_prior, [0.5])[0]

    # (B) α-based gating with histogram
    log_alpha_med_k = np.log(np.clip(alpha_med_k, alpha_min, alpha_max))
    bin_idx = np.searchsorted(alpha_bin_edges, log_alpha_med_k, side='right') - 1
    bin_idx = np.clip(bin_idx, 0, n_bins_alpha - 1)
    alpha_hist_counts[bin_idx] += 1.0

    cdf_bins = np.cumsum(alpha_hist_counts)
    cdf_bins /= cdf_bins[-1]
    F_k = cdf_bins[bin_idx]

    if F_k <= F_quiet:
        g_alpha = 1.0
        regime_flag[k] = 0   # quiet / learning
    elif F_k >= F_burst:
        g_alpha = g_min
        regime_flag[k] = 1   # high-vol / burst
    else:
        r = (F_burst - F_k) / (F_burst - F_quiet + 1e-12)
        g_alpha = g_min + (1.0 - g_min) * r
        regime_flag[k] = 0   # treat as learning-ish

    g_k = max(g_alpha, g_min)

    # (C) Liu–West on θ (soft-gated by g_k)
    phi = np.log(np.clip(theta, theta_eps, None))

    if free_idx.size > 0:
        a_k  = 1.0 - g_k * (1.0 - a_lw)
        h2_k = g_k * h2_lw

        phi_bar_free = np.sum(phi[:, free_idx] * w_lin_prior[:, None], axis=0)

        diff_free = phi[:, free_idx] - phi_bar_free[None, :]
        V_free    = diff_free.T @ (diff_free * w_lin_prior[:, None])
        V_free    = 0.5 * (V_free + V_free.T) + 1e-12 * np.eye(free_idx.size)

        L_free   = np.linalg.cholesky(V_free)
        eps_free = rng.standard_normal((Np, free_idx.size)) @ (np.sqrt(h2_k) * L_free.T)

        phi[:, free_idx] = (
            a_k * phi[:, free_idx] +
            (1.0 - a_k) * phi_bar_free[None, :] +
            eps_free
        )

        phi[:, free_idx] = np.clip(
            phi[:, free_idx],
            phi_lo[None, free_idx],
            phi_hi[None, free_idx]
        )
    if fixed_idx.size > 0:
        phi[:, fixed_idx] = np.log(np.clip(theta_lo[fixed_idx], theta_eps, None))

    theta = np.exp(phi)

    # (D) Build per-particle CONTINUOUS-TIME matrices
    A_c_i, B_u_i, B_To_i, B_Us_i = continuous_from_theta(theta)

    # Kalman predict: mean via Euler sub-stepping, covariance via approx Φ = I + A_c * dt_total
    # ----------------------------------------------------------------------
    # Mean prediction with substeps
    m_pred = m.copy()
    for _ in range(n_subs):
        dx = np.einsum('nij,nj->ni', A_c_i, m_pred)
        dx += uk * B_u_i + Tok * B_To_i + Usk * B_Us_i
        m_pred += dx * dt_sub

    # Covariance prediction (one-shot Euler approximation over dt_total)
    Phi_approx = I5_batch + A_c_i * dt_total   # (Np,5,5)
    P_pred = np.einsum('nij,njk,nlk->nil', Phi_approx, P, Phi_approx)

    # Single dynamic scalar on Q (scaled by α)
    scale_sq = np.exp(2.0 * log_alpha)
    qdiag    = scale_sq[:, None] * Q0_diag[None, :]
    for s in range(5):
        P_pred[:, s, s] += qdiag[:, s]

    # (E) Prior predictive Y (1-step ahead)
    y_pred = (m_pred @ C_y.T).ravel()
    S      = np.einsum('ij,njk,kl->n', C_y, P_pred, C_y.T) + R[0, 0]
    S      = np.maximum(S, 1e-12)

    mu_k  = np.sum(w_lin_prior * y_pred)
    var_k = np.sum(w_lin_prior * (y_pred**2 + S)) - mu_k**2
    var_k = max(var_k, 1e-12)
    Y_pred_mean[k] = mu_k
    Y_pred_std[k]  = np.sqrt(var_k)
    z_std_innov[k] = (yk - mu_k) / np.sqrt(var_k)

    # (F) Weight update
    innov = yk - y_pred
    ll    = const_ll - 0.5*np.log(S) - 0.5*(innov**2)/S
    logw  = logw + ll
    logw  = logw - logsumexp(logw)
    w_lin_post = np.exp(logw)

    # (G) Kalman update
    K = (P_pred @ C_y.T) / S[:, None, None]
    m = m_pred + (K.reshape(Np, 5) * innov[:, None])
    KC = np.matmul(K, C_y)
    P  = np.matmul(I5_batch - KC, P_pred)

    # Summaries at time k
    Ti_draws = sample_from_weighted_gaussian_mixture(
        m[:, 0], P[:, 0, 0], w_lin_post, Nsamp_mixture_state, rng
    )
    TiC = Ti_draws - 273.15
    Ti_lo[k], Ti_md[k], Ti_hi[k] = np.quantile(TiC, [q_lo, q_md, q_hi])
    Alpha_lo[k], Alpha_md[k], Alpha_hi[k] = wquantile(alpha, w_lin_post, [q_lo, q_md, q_hi])


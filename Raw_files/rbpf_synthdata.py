    g_im, g_ie, g_ih, g_is, g_ea = theta_full[5], theta_full[6], theta_full[7], theta_full[8], theta_full[9]
    g_ia = 0.0

    invC = np.array([1.0/Ci, 1.0/Cm, 1.0/Ce, 1.0/Ch, 1.0/Cs])

    # --- Construct Continuous A matrix (5x5) ---
    d0 = -(g_im + g_ie + g_ih + g_is + g_ia)
    d1 = -g_im
    d2 = -(g_ie + g_ea)
    d3 = -g_ih
    d4 = -g_is

    A_c = np.zeros((5,5))
    A_c[0,:] = [d0,   g_im, g_ie, g_ih, g_is]

def discrete_from_theta_single(theta_full):
    Ci, Cm, Ce, Ch, Cs = (
        theta_full[0],
        theta_full[1],
        theta_full[2],
        theta_full[3],
        theta_full[4],
    )
    g_im, g_ie, g_ih, g_is, g_ea = (
        theta_full[5],
        theta_full[6],
        theta_full[7],
        theta_full[8],
        theta_full[9],
    )
    g_ia = 0.0

    invC = np.array([1.0 / Ci, 1.0 / Cm, 1.0 / Ce, 1.0 / Ch, 1.0 / Cs])

    # --- 1. Construct Continuous Dynamics Matrix A_c ---
    # Rows are scaled by 1/C after conductance assembly
    d0 = -(g_im + g_ie + g_ih + g_is + g_ia)
    d1 = -g_im
    d2 = -(g_ie + g_ea)
    d3 = -g_ih
    d4 = -g_is

    A_c = np.zeros((5, 5))
    A_c[0, :] = [d0, g_im, g_ie, g_ih, g_is]
    A_c[1, :] = [g_im, d1, 0, 0, 0]
    A_c[2, :] = [g_ie, 0, d2, 0, 0]
    A_c[3, :] = [g_ih, 0, 0, d3, 0]
    A_c[4, :] = [g_is, 0, 0, 0, d4]

    # Apply 1/C scaling to rows
    A_c = (A_c.T * invC).T

    # --- 2. Construct Continuous Input Matrix B_c ---
    # Columns correspond to: [u (heater), T_out, U_s (solar)]

    B_u  = B_d[:, 0]  # heater input
    B_To = B_d[:, 1]  # outdoor temperature
    B_Us = B_d[:, 2]  # solar

    # Initial state guess: set all nodes to first measured T_in
    if x0 is None:
        x = np.array([Y_meas[0]] * 5, float)
    else:
        x = np.array(x0, float)

    y_hat = np.empty_like(Y_meas, dtype=float)

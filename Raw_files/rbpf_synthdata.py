def load_dwd_temperature(path: Path) -> pd.DataFrame:
    # Added on_bad_lines='skip' to handle malformed rows/footers
    df = pd.read_csv(path, sep=";", decimal=",", dtype=str,
                     na_values=["-999", "-9999", "-999.0", "", "NA", "NaN"],
                     on_bad_lines='skip')

    if "MESS_DATUM" not in df.columns:
        # Sometimes DWD files have leading spaces in headers; strip them just in case
        df.columns = df.columns.str.strip()

    if "MESS_DATUM" not in df.columns:
        raise ValueError(f"Temperature file missing 'MESS_DATUM'. Found: {df.columns}")

    cand = [c for c in df.columns if c.upper().startswith("TT")]
    if not cand:
        raise ValueError("Temperature file missing 'TT_10' (°C).")
    df = df.rename(columns={cand[0]: "TT_10"})

    df["Timestamp"] = pd.to_datetime(df["MESS_DATUM"], format="%Y%m%d%H%M", errors="coerce")
    df["TT_10"] = pd.to_numeric(df["TT_10"], errors="coerce")
    df = df.dropna(subset=["Timestamp", "TT_10"]).reset_index(drop=True)
    return df[["Timestamp", "TT_10"]].sort_values("Timestamp")


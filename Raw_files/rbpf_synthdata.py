        U_s_loc   = np.asarray(U_s_arr if 'U_s_arr' in globals() else U_s, float).ravel()

        # Measurement matrix for Ti
        C_y_arr = np.asarray(C_y if 'C_y' in globals() else C_meas, float).reshape(1, -1)

        # Container for per-segment RMSEs
        openloop_rmse = {name: [] for name in theta_final.keys()}

        # ----------------------------------------
        # 5) Loop over quiet segments and methods
        # ----------------------------------------
        for seg_idx, (k0, k1) in enumerate(quiet_segments, start=1):
            seg_len   = k1 - k0 + 1
            seg_hours = seg_len * float(dt_seconds) / 3600.0

            if has_ts:
                t0 = ts[k0]
                t1 = ts[k1]
                print(f"\nSegment {seg_idx}: idx [{k0}:{k1}] "
                      f"(len={seg_len} steps, {seg_hours:.2f} h, {t0} → {t1})")
            else:
                print(f"\nSegment {seg_idx}: idx [{k0}:{k1}] "
                      f"(len={seg_len} steps, {seg_hours:.2f} h)")

            # True initial state at start of segment
            x0_true     = X_arr[k0, :].astype(float)
            Ti_true_seg = Ti_true_K[k0:k1+1]

            for method_name, theta_est in theta_final.items():
                # Build discrete-time model from final parameters (ZOH)
                Phi, Gu, GTo, GUs = discrete_system_zoh(theta_est, dt_seconds)

                x = x0_true.copy()
                y_hat_seg = np.zeros(seg_len, float)
                stable = True

                for i in range(seg_len):
                    # Output at current state
                    y_hat_seg[i] = float(C_y_arr @ x)

                    if i < seg_len - 1:
                        idx_global = k0 + i
                        uk  = u_arr_loc[idx_global]
                        Tok = T_out_loc[idx_global]
                        Usk = U_s_loc[idx_global]

                        # ZOH-discretized open-loop update (no latent force, no noise)
                        x = Phi @ x + Gu * uk + GTo * Tok + GUs * Usk

                        # Basic stability / sanity check
                        if (not np.all(np.isfinite(x))) or np.any(np.abs(x) > 1e6):
                            stable = False
                            break

                if not stable:
                    rmse_val = np.nan
                    print(f"  {method_name:4s}: simulation unstable (RMSE = NaN)")
                else:
                    # RMSE in Kelvin (same as °C difference)
                    rmse_val = float(np.sqrt(np.mean((y_hat_seg - Ti_true_seg)**2)))
                    print(f"  {method_name:4s}: RMSE_open_loop = {rmse_val:.4f} K "
                          f"(= {rmse_val:.4f} °C)")
                openloop_rmse[method_name].append(rmse_val)

        # ----------------------------------------
        # 6) Summary across segments
        # ----------------------------------------
        print("\nSummary over all quiet segments (RMSE_open_loop in °C/K):")
        for method_name, vals in openloop_rmse.items():
            vals_arr = np.asarray([v for v in vals if np.isfinite(v)], float)
            if vals_arr.size == 0:
                print(f"  {method_name:4s}: no finite RMSE values.")
            else:
                mean_rmse = float(vals_arr.mean())
                med_rmse  = float(np.median(vals_arr))
                min_rmse  = float(vals_arr.min())
                max_rmse  = float(vals_arr.max())
                print(f"  {method_name:4s}: "
                      f"mean={mean_rmse:.4f}, median={med_rmse:.4f}, "
                      f"min={min_rmse:.4f}, max={max_rmse:.4f}")


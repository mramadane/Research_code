def align_to_grid(df, grid, col_name):
    # Ensure sorted unique index
    df = df.sort_values("Timestamp").drop_duplicates(subset=["Timestamp"])
    df = df.set_index("Timestamp")

    # Reindex with nearest tolerance.
    # We allow a tolerance of dt/2 so we don't grab data too far away.
    tol = pd.Timedelta(seconds=dt_seconds/2)
    aligned = df.reindex(grid, method='nearest', tolerance=tol)

    # Fill remaining NaNs (gaps > tolerance) with ffill/bfill or 0
    # Here we forward fill to keep causal structure, then backfill start
    aligned[col_name] = aligned[col_name].ffill().bfill()
    return aligned[col_name].values


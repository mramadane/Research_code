def tf_h2_error(theta_est, theta_true, channel, w_grid):
    """
    H2-like energy error between learned and true TF for one channel.

    Approximate:
        ||G_est - G_true||_H2 ≈ sqrt( mean_ω |G_est(e^{jw}) - G_true(e^{jw})|^2 )
    over the given frequency grid w_grid.
    """
    sys_true = build_siso_ss_from_theta(theta_true, channel)
    sys_est  = build_siso_ss_from_theta(theta_est,  channel)

    # python-control: mag, phase, omega
    mag_true, phase_true, _ = control.freqresp(sys_true, w_grid)
    mag_est,  phase_est,  _ = control.freqresp(sys_est,  w_grid)

    mag_true  = mag_true.squeeze()
    mag_est   = mag_est.squeeze()
    phase_true = phase_true.squeeze()
    phase_est  = phase_est.squeeze()

    # Complex FRFs
    G_true = mag_true * np.exp(1j * phase_true)
    G_est  = mag_est  * np.exp(1j * phase_est)

    diff = G_est - G_true
    # RMS of complex error over frequency grid (H2-like)
    E = np.sqrt(np.mean(np.abs(diff)**2))

    return E


def tf_h2_norm(theta, channel, w_grid):
    """
    H2-like norm of a single-channel FRF:
        ||G||_H2 ≈ sqrt( mean_ω |G(e^{jw})|^2 )
    """
    sys = build_siso_ss_from_theta(theta, channel)
    mag, phase, _ = control.freqresp(sys, w_grid)
    G = mag.squeeze() * np.exp(1j * phase.squeeze())
    return np.sqrt(np.mean(np.abs(G)**2))



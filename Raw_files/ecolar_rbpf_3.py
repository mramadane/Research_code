def filter_multi_column_changes(df, cols, time_col="Timestamp"): #We only keep rows where a changes in columns in col occurs
    df = df.sort_values(time_col).reset_index(drop=True)
    shifted = df[cols].shift(1)
    changed = (df[cols] != shifted).any(axis=1)
    return df[changed].reset_index(drop=True)

def find_and_split_gaps(
    df: pd.DataFrame,
    min_lag_seconds: int,
    time_col: str = "Timestamp"
) -> list[pd.DataFrame]:
    """
    Identifies time gaps in a DataFrame, prints a report, and splits the
    DataFrame into a list of continuous segments.
    """
    if time_col not in df.columns:
        raise ValueError(f"Time column '{time_col}' not found in the DataFrame.")

    # 1. Ensure data is sorted by timestamp
    df_sorted = df.copy()
    df_sorted[time_col] = pd.to_datetime(df_sorted[time_col])
    df_sorted = df_sorted.sort_values(by=time_col).reset_index(drop=True)

    # 2. Calculate the time difference between consecutive rows in seconds
    time_diffs = df_sorted[time_col].diff().dt.total_seconds()

    # 3. Find the indices where a new segment begins (i.e., after a gap)
    gap_indices = df_sorted.index[time_diffs > min_lag_seconds].tolist()

    # 4. Report the found gaps
    if not gap_indices:
        print(f"No gaps larger than {min_lag_seconds} seconds found.")
        return [df_sorted]  # Return the original sorted DataFrame in a list

    #print(f" Found {len(gap_indices)} gap(s) larger than {min_lag_seconds} seconds.")
    #print("-" * 60)
    for idx in gap_indices:
        gap_start_time = df_sorted.loc[idx - 1, time_col]
        gap_end_time = df_sorted.loc[idx, time_col]
        actual_gap = (gap_end_time - gap_start_time).total_seconds()

        #print(f"Gap detected between:\n  - Start: {gap_start_time}\n  - End:   {gap_end_time}")
        #print(f"  - Duration: {actual_gap:,.2f} seconds\n")
    #print("-" * 60)


    # 5. Decompose the DataFrame into a list of continuous DFs
    split_points = [0] + gap_indices + [len(df_sorted)]
    list_of_dfs = []

    # Create pairs of start/end indices for slicing
    for i in range(len(split_points) - 1):
        start_idx = split_points[i]
        end_idx = split_points[i+1]
        segment = df_sorted.iloc[start_idx:end_idx].reset_index(drop=True)
        list_of_dfs.append(segment)

    print(f"DataFrame has been split into {len(list_of_dfs)} continuous segments.")
    return list_of_dfs

def interpolate_to_grid(df, dt_seconds=60, time_col="Timestamp", interpolate_cols=None):
    df = df.copy()
    df[time_col] = pd.to_datetime(df[time_col])
    df = df.sort_values(time_col, ignore_index=True)

    # Original timestamps in epoch seconds
    t_orig = df[time_col].astype("int64") // 10**9
    start  = int(np.ceil(t_orig.iloc[0] / dt_seconds) * dt_seconds)
    end    = int(np.floor(t_orig.iloc[-1] / dt_seconds) * dt_seconds)
    t_new  = np.arange(start, end + 1, dt_seconds, dtype=np.int64)

    # New timestamp index
    new_timestamps = pd.to_datetime(t_new, unit="s")
    out = {time_col: new_timestamps}

    # Handle columns to interpolate
    if interpolate_cols is None:
        # Default to all numeric (excluding time_col)
        interpolate_cols = df.select_dtypes(include=np.number).columns.tolist()
        interpolate_cols = [c for c in interpolate_cols if c != time_col]

    for col in interpolate_cols:
        out[col] = np.interp(t_new, t_orig.to_numpy(), df[col].to_numpy())

    # Build interpolated DataFrame
    df_interp = pd.DataFrame(out)
    other_cols = [c for c in df.columns if c not in interpolate_cols + [time_col]]
    if other_cols:
        df_aux = df[[time_col] + other_cols].set_index(time_col)
        df_aux = df_aux.reindex(df_interp[time_col], method="ffill").reset_index(drop=True)
        df_interp[other_cols] = df_aux[other_cols]

    return df_interp


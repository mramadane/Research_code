def simulate_open_loop_final_4state(theta_vec, u, T_out, T_or, y_start, dt_total):
    # Unpack 10 parameters
    # 0:Ca, 1:Cwo, 2:Cwi, 3:Ch, 4:gao, 5:gai, 6:gao_ext, 7:gwo_ext, 8:gwi_oth, 9:gah
    Ca, Cwo, Cwi, Ch, gao, gai, gao_ext, gwo_ext, gwi_oth, gah = theta_vec

    # --- Build Continuous Matrices A (4x4) and B (4x3) ---
    A = np.zeros((4,4))

    # Row 0: Air
    A[0,0] = -(gao + gai + gah + gao_ext) / Ca
    A[0,1] = gao / Ca
    A[0,2] = gai / Ca
    A[0,3] = gah / Ca

    # Row 1: Wall Out
    A[1,0] = gao / Cwo
    A[1,1] = -(gao + gwo_ext) / Cwo

    # Row 2: Wall In
    A[2,0] = gai / Cwi
    A[2,2] = -(gai + gwi_oth) / Cwi

    # Row 3: Heater
    A[3,0] = gah / Ch
    A[3,3] = -gah / Ch

    # Matrix B (Inputs: u, T_out, T_or)
    B = np.zeros((4,3))
    B[0,1] = gao_ext / Ca      # T_out -> Air
    B[1,1] = gwo_ext / Cwo     # T_out -> Wall Out
    B[2,2] = gwi_oth / Cwi     # T_or  -> Wall In
    B[3,0] = 1.0 / Ch          # u     -> Heater Node

    # --- Simulation Loop with Sub-Stepping ---
    N_sim = len(u)
    X_sim = np.zeros((N_sim, 4))

    # Initial State: Assume everything starts at measured air temp
    x = np.array([y_start, y_start, y_start, y_start])

    # Sub-step settings (match RBPF)
    n_subs = 30
    dt_sub = dt_total / n_subs

    for k in range(N_sim):
        X_sim[k] = x

        if k < N_sim - 1:
            # Current inputs
            U_vec = np.array([u[k], T_out[k], T_or[k]])

            # Micro-loop integration
            for _ in range(n_subs):
                dx = A @ x + B @ U_vec
                x += dx * dt_sub

    return X_sim[:, 0], X_sim # Return Air Temp, All States


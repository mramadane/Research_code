# ------------------------
# Helper: Winkler score for 95% intervals
# ------------------------
def winkler_score(y, lo, hi, alpha=0.05):
    """
    Compute mean Winkler score for (1-alpha) central prediction intervals.
    y, lo, hi: 1D arrays (same length, same units).
    """
    y  = np.asarray(y,  float)
    lo = np.asarray(lo, float)
    hi = np.asarray(hi, float)

    ok = np.isfinite(y) & np.isfinite(lo) & np.isfinite(hi) & (lo <= hi)
    if not np.any(ok):
        return np.nan

    y, lo, hi = y[ok], lo[ok], hi[ok]
    width = hi - lo

    score = np.empty_like(width)
    inside = (y >= lo) & (y <= hi)
    below  = (y < lo)
    above  = (y > hi)

    # Inside interval
    score[inside] = width[inside]
    # Below
    score[below]  = width[below] + (2.0/alpha)*(lo[below] - y[below])
    # Above
    score[above]  = width[above] + (2.0/alpha)*(y[above] - hi[above])

    return float(np.mean(score))


# ------------------------
# Helper: TF error metrics (H2-like, H1-like, H∞-like)
# ------------------------
def tf_error_metrics(theta_est, theta_true, channel, w_grid):
    """
    Compute frequency-domain errors between TF(theta_est) and TF(theta_true)
    for a given input channel over the given frequency grid w_grid.
    Returns a dict with absolute and relative H2, H1, H∞-like errors.
    """
    sys_true = build_siso_ss_from_theta(theta_true, channel)
    sys_est  = build_siso_ss_from_theta(theta_est,  channel)

    mag_true, phase_true, _ = control.freqresp(sys_true, w_grid)
    mag_est,  phase_est,  _ = control.freqresp(sys_est,  w_grid)

    G_true = mag_true.squeeze() * np.exp(1j*phase_true.squeeze())
    G_est  = mag_est.squeeze()  * np.exp(1j*phase_est.squeeze())

    diff     = G_est - G_true
    abs_diff = np.abs(diff)
    abs_true = np.abs(G_true)

    # H2-like (RMS over freq)
    h2_err  = math.sqrt(np.mean(abs_diff**2))
    h2_norm = math.sqrt(np.mean(abs_true**2))

    # H1-like (mean abs error / mean abs magnitude)
    h1_err  = float(np.mean(abs_diff))
    h1_norm = float(np.mean(abs_true))

    # H∞-like (max abs error / max abs magnitude)
    hinf_err  = float(np.max(abs_diff))
    hinf_norm = float(np.max(abs_true))

    return {
        "H2_err":   h2_err,
        "H2_rel":   h2_err / h2_norm if h2_norm > 0 else np.nan,
        "H1_err":   h1_err,
        "H1_rel":   h1_err / h1_norm if h1_norm > 0 else np.nan,
        "Hinf_err": hinf_err,
        "Hinf_rel": hinf_err / hinf_norm if hinf_norm > 0 else np.nan,
    }



# SSE objective: KF 1-step-ahead errors on Ti
# ------------------------------------------------------------------
def sse_g4(g4):
    theta_full = theta_from_g4(g4)
    Phi, Gam_u, Gam_To, Gam_Us = discrete_from_theta_single(theta_full)

    m = np.array([Y_meas[0]] * 5, float)
    P = np.diag(np.full(5, 0.2**2))

    sse = 0.0
    for k in range(1, len(Y_meas)):
        uk  = u[k-1]
        Tok = T_out_K[k-1]
        Usk = U_s[k-1]
        yk  = float(Y_meas[k])

        # predict
        m = Phi @ m + uk * Gam_u + Tok * Gam_To + Usk * Gam_Us
        P = Phi @ P @ Phi.T + Q_true

        # 1-step prediction of measurement
        y_pred = (C_meas @ m).item()
        err = yk - y_pred
        sse += err * err

        # KF update
        S = (C_meas @ P @ C_meas.T + R_true).item()
        S = max(S, 1e-12)
        K = (P @ C_meas.T) / S
        m = m + K.flatten() * err
        P = (np.eye(5) - K @ C_meas) @ P

    return sse


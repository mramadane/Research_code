    G30 = g_ih; G33 = -g_ih
    G40 = g_is; G44 = -g_is

    A0 = np.array([G00, G01, G02, G03, G04]) * invC[0]
    A1 = np.array([G10, G11, 0.0, 0.0, 0.0]) * invC[1]
    A2 = np.array([G20, 0.0,  G22, 0.0, 0.0]) * invC[2]
    A3 = np.array([G30, 0.0, 0.0,  G33, 0.0]) * invC[3]
    A4 = np.array([G40, 0.0, 0.0, 0.0,  G44]) * invC[4]
    A_c = np.stack([A0, A1, A2, A3, A4], axis=0)


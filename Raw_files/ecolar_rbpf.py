def systematic_resample(w, rng):
    N = w.size
    u0 = (rng.random() + np.arange(N)) / N
    cs = np.cumsum(w)
    idx = np.empty(N, int)
    i = j = 0
    while i < N:
        if u0[i] < cs[j]:
            idx[i] = j
            i += 1
        else:
            j += 1
    return idx

def wquantile(x, w, qs):
    x = np.asarray(x); w = np.asarray(w)
    i = np.argsort(x)
    xs, ws = x[i], w[i]
    cw = np.cumsum(ws)
    if cw[-1] > 0: cw /= cw[-1]
    else: cw = np.linspace(1/len(ws), 1, len(ws))
    return np.interp(qs, cw, xs)

def logsumexp(v):
    m = np.max(v)
    return m + np.log(np.sum(np.exp(v - m)))

def sample_from_weighted_gaussian_mixture(means, variances, weights, Nsamp, rng):
    weights = np.asarray(weights, float)
    cw = np.cumsum(weights)
    if cw[-1] > 0: cw /= cw[-1]
    else: cw = np.linspace(1/len(weights), 1, len(weights))
    u = (rng.random(Nsamp) + np.arange(Nsamp)) / Nsamp
    idx = np.searchsorted(cw, u, side='right')
    idx = np.clip(idx, 0, len(weights)-1)
    stds = np.sqrt(np.maximum(variances[idx], 1e-12))
    return means[idx] + stds * rng.standard_normal(Nsamp)

# --- UPDATED HELPER FOR 4 STATES ---
def get_continuous_matrices_4state(theta_arr):
    """
    Returns Continuous-time A_c and B_c for N particles (4-State Model).
    States: [T_air, T_wo, T_wi, T_heater]
    Output shapes: (Np, 4, 4) and (Np, 4, 3)
    """
    Np_loc = theta_arr.shape[0]

    # Unpack columns (Now 10 parameters)
    # 0:Ca, 1:Cwo, 2:Cwi, 3:Cheater,
    # 4:gao, 5:gai, 6:gao_ext, 7:gwo_ext, 8:gwi_oth, 9:gah
    Ca      = theta_arr[:, 0]
    Cwo     = theta_arr[:, 1]
    Cwi     = theta_arr[:, 2]
    Ch      = theta_arr[:, 3]
    gao     = theta_arr[:, 4]
    gai     = theta_arr[:, 5]
    gao_ext = theta_arr[:, 6]
    gwo_ext = theta_arr[:, 7]
    gwi_oth = theta_arr[:, 8]
    gah     = theta_arr[:, 9]

    # --- Build A_c (Dynamics 4x4) ---
    A = np.zeros((Np_loc, 4, 4))

    # Row 0: Air (Connects to WO, WI, Heater, Outside)
    A[:,0,0] = -(gao + gai + gah + gao_ext) / Ca
    A[:,0,1] = gao / Ca
    A[:,0,2] = gai / Ca
    A[:,0,3] = gah / Ca   # From Heater

    # Row 1: Wall Out (Connects to Air, Outside)
    A[:,1,0] = gao / Cwo
    A[:,1,1] = -(gao + gwo_ext) / Cwo

    # Row 2: Wall In (Connects to Air, Other Room)
    A[:,2,0] = gai / Cwi
    A[:,2,2] = -(gai + gwi_oth) / Cwi

    # Row 3: Heater (Connects to Air)
    A[:,3,0] = gah / Ch
    A[:,3,3] = -gah / Ch

    # --- Build B_c (Inputs 4x3) ---
    # Input Vector U: [u (Power), T_out, T_or]
    B = np.zeros((Np_loc, 4, 3))

    # Row 0: Air (Infiltration) - Note: Power no longer goes here directly
    B[:,0,1] = gao_ext / Ca      # T_out -> Air

    # Row 1: Wall Out (Outside Temp)
    B[:,1,1] = gwo_ext / Cwo     # T_out -> Wall Out

    # Row 2: Wall In (Other Room Temp)
    B[:,2,2] = gwi_oth / Cwi     # T_or -> Wall In

    # Row 3: Heater Node (Gets the Power Input)
    B[:,3,0] = 1.0 / Ch          # u -> Heater Node

    return A, B


    A = np.zeros((4,4))

    # Row 0: Air
    A[0,0] = -(gao + gai + gah + gao_ext) / Ca
    A[0,1] = gao / Ca
    A[0,2] = gai / Ca
    A[0,3] = gah / Ca

    # Row 1: Wall Out
    A[1,0] = gao / Cwo
    A[1,1] = -(gao + gwo_ext) / Cwo

    # Row 2: Wall In
    A[2,0] = gai / Cwi
    A[2,2] = -(gai + gwi_oth) / Cwi

    # Row 3: Heater
    A[3,0] = gah / Ch
    A[3,3] = -gah / Ch

    # Matrix B (Inputs: u, T_out, T_or)
    B = np.zeros((4,3))
    B[0,1] = gao_ext / Ca      # T_out -> Air
    B[1,1] = gwo_ext / Cwo     # T_out -> Wall Out
    B[2,2] = gwi_oth / Cwi     # T_or  -> Wall In
    B[3,0] = 1.0 / Ch          # u     -> Heater Node

    # --- Simulation Loop with Sub-Stepping ---
    N_sim = len(u)
    X_sim = np.zeros((N_sim, 4))

    # --- CORRECTED INITIALIZATION ---
    # We use the first value of the PASSED T_out array
    T_out_start = T_out[0]

    # [T_air, T_wo, T_wi, T_h]
    # T_wo initialized to T_out_start, others to y_start
    x = np.array([y_start, T_out_start, y_start, y_start], dtype=float)

    # Sub-step settings (match RBPF)
    n_subs = 30
    dt_sub = dt_total / n_subs

    for k in range(N_sim):
        X_sim[k] = x

        if k < N_sim - 1:
            # Current inputs
            U_vec = np.array([u[k], T_out[k], T_or[k]])

            # Micro-loop integration
            for _ in range(n_subs):
                dx = A @ x + B @ U_vec
                x += dx * dt_sub

    return X_sim[:, 0], X_sim # Return Air Temp, All States


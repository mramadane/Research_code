# ─────────────────────────────────────────────────────────────────────────────
# 4-state CT model: Air + Two Walls + Heater Node (10 Parameters)
# ─────────────────────────────────────────────────────────────────────────────

# Keys (10 parameters now)
POS_KEYS_4C = [
    "C_air", "C_wall_out", "C_wall_in", "C_heater",  # Capacities [J/K]
    "g_air_ow", "g_air_iw", "g_air_outside",         # Air connectivity [W/K]
    "g_wo_outside", "g_wi_other",                    # Walls -> Boundary [W/K]
    "g_air_heater"                                   # Heater -> Air [W/K]
]

def build_system_ct_4nodes(pos):
    """
    Return continuous-time (A_c, B_u, F_out, F_or, R) for the 4-state model.
    States: [T_air, T_wall_out, T_wall_in, T_heater]
    """
    # Unpack Capacities
    Ca  = float(pos["C_air"])
    Cwo = float(pos["C_wall_out"])
    Cwi = float(pos["C_wall_in"])
    Ch  = float(pos["C_heater"])

    # Unpack Conductances
    g_ao    = float(pos["g_air_ow"])
    g_ai    = float(pos["g_air_iw"])
    g_ao_ut = float(pos["g_air_outside"]) # Infiltration
    g_wo    = float(pos["g_wo_outside"])
    g_wi    = float(pos["g_wi_other"])
    g_ah    = float(pos["g_air_heater"])

    # Inverse Capacitance Matrix
    # Order: Air, Wall_Out, Wall_In, Heater
    Cinv = np.diag(1.0 / np.maximum([Ca, Cwo, Cwi, Ch], 1e-12))

    # Symmetric Conductance Matrix R (4x4)
    # 0: Air, 1: WO, 2: WI, 3: Heater
    R = np.zeros((4, 4), float)

    # Off-diagonals (Couplings)
    R[0, 1] = R[1, 0] = g_ao  # Air <-> Wall Out
    R[0, 2] = R[2, 0] = g_ai  # Air <-> Wall In
    R[0, 3] = R[3, 0] = g_ah  # Air <-> Heater

    # Diagonals (Sum of connections + leaks)
    R[0, 0] = -(g_ao + g_ai + g_ah + g_ao_ut) # Air loses to walls, heater, and outside
    R[1, 1] = -(g_ao + g_wo)                  # Wall Out loses to Air and Outside
    R[2, 2] = -(g_ai + g_wi)                  # Wall In loses to Air and Other Room
    R[3, 3] = -(g_ah)                         # Heater only loses to Air (assuming no back-loss)

    # Continuous-time state matrix
    A_c = Cinv @ R

    # Inputs
    # B_u: Electrical power u enters the HEATER node (State 3)
    B_u = Cinv @ np.array([[0.0, 0.0, 0.0, 1.0]]).T

    # F_out: Outside temp affects Air (infiltration) and Wall Out
    F_out  = Cinv @ np.array([[g_ao_ut, 0.0, 0.0, 0.0]]).T
    F_out += Cinv @ np.array([[0.0, g_wo, 0.0, 0.0]]).T

    # F_or: Other Room temp affects Wall In
    F_or = Cinv @ np.array([[0.0, 0.0, g_wi, 0.0]]).T

    return A_c, B_u, F_out, F_or, R

# ─────────────────────────────────────────────────────────────────────────────
# ZOH Discretization (Same logic, handles n=4 automatically)
# ─────────────────────────────────────────────────────────────────────────────
def c2d_zoh(A, B, dt):
    n = A.shape[0]
    m = B.shape[1]
    C_dummy = np.eye(n)
    D_dummy = np.zeros((n, m))
    Ad, Bd, _, _, _ = cont2discrete((A, B, C_dummy, D_dummy), dt, method='zoh')
    return Ad, Bd

def build_system_d_4nodes(pos, dt):
    """Bundle CT → DT system using ZOH discretization."""
    A_c, B_u, F_out, F_or, R = build_system_ct_4nodes(pos)

    # Stack inputs: [u, T_out, T_or]
    B = np.column_stack([B_u, F_out, F_or])

    Phi, Gamma = c2d_zoh(A_c, B, dt)
    return Phi, Gamma, R

